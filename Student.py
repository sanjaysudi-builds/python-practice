# Storing the data of one student

name = input("Enter student name: ")
college = input("Enter college name: ")
course = input("Enter course name: ")
percentage = input("Enter percentage: ")

student = [name,
           college, 
           course, 
           percentage]

print("\nStudent Record:")
print(student)

